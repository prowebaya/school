
import { createSlice } from '@reduxjs/toolkit';

export const examScheduleSlice = createSlice({
  name: 'examSchedule',
  initialState: {
    examSchedulesList: [],
    loading: false,
    error: null,
  },
  reducers: {
    getRequest: (state) => {
      state.loading = true;
      state.error = null;
    },
    getSuccess: (state, action) => {
      state.examSchedulesList = action.payload;
      state.loading = false;
    },
    getError: (state, action) => {
      state.error = action.payload;
      state.loading = false;
    },
    stuffDone: (state) => {
      state.loading = false;
    },
    clearError: (state) => {
      state.error = null;
    },
  },
});

export const { getRequest, getSuccess, getError, stuffDone, clearError } = examScheduleSlice.actions;
export default examScheduleSlice.reducer;
